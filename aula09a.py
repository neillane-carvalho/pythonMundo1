# Fatiamento - pegar um pedaço da string
frase = "Curso em Video Python"
print(frase[9])  # mostra a letra no indice 9
print(frase[9:14])  # mostra de 9 a 13 - exclui o 14
print(frase[:5])  # mostra do primeiro até o 4
print(frase[15:])  # mostra do 15 até o ultimo
print(frase[9:21:2])  # mostra do 9 ao 21 pulando de 2 em 2
print(frase[9::3])  # ele vai de 9 até o final pulando de 3 em 3
