n1 = input('Digite algo: ')
print(n1.isnumeric())  # asks if it's a number
print(n1.isalpha())  # asks if it's a letter
print(n1.isalnum())  # asks if it's a number or letter
print(n1.isspace())  # asks if it's a blank space
print(n1.islower())  # asks if all are lower cases
print(n1.isupper())  # asks if all are upper cases
print(n1.isdigit())  # asks if there are all digits
