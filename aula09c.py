#transformação
frase = 'Curso em Video Python'
print(frase)
print(frase.replace('Python', 'Android')) #substitui
print(frase.upper()) #o que for maiusculo matem o que não for transforma
print(frase.lower()) #o que for minusculo matem o que não for transforma
print(frase.capitalize()) #tudo minuscolo exeto a primeira letra
print(frase.title()) # transforma em maiusculo a 1 letra de todas as palavras
frase2 = '  Curso Python  '
print(frase2)
print(frase2.strip()) #remove espaços inúteis
print(frase2.rstrip()) #remove espaços inúteis da direita
print(frase2.lstrip()) #remove espaços inúteis da esquerda

